public class PokemonSuper {

    private String jump;
    private String walk;


    public void Jumping(String jump) {
        this.jump = jump;
    }
    public void Walking (String walk) {
        this.walk = walk;
    }


    public String getJump() {
        return jump;
    }

    public void setJump(String jump) {
        this.jump = jump;
    }

    public String getWalk() {
        return walk;
    }

    public void setWalk(String walk) {
        this.walk = walk;
    }


}
